// write a typescript function called isEven that takes a number as a parameter and returns true if the number is even and false otherwise
function isEven(a) {
  return a % 2 === 0;
}
console.log(isEven(5));
// write a typescript function called isDivisible by 4 and 8 that takes a number as a parameter and returns true if the number is divisible and false otherwise
function isDivisible(a) {
  return a % 4 == 0 || a % 8 === 0;
}
console.log(isDivisible(19));
